int SumNeighborRecursiveMethod(int currentNumber)
{

    if (currentNumber == 0)
    {
        return 0;
    }
    else if (currentNumber == 1)
    {
        return 1;
    }
    else
    {
        return currentNumber + SumNeighborRecursiveMethod(currentNumber - 1);
    }


}


int SumNeighborStandardMethod(int limit)
{
    int solution = 0;
    for (int i = 1; i <= limit; i++)
    {
        solution += i;
    }
    return solution;
}